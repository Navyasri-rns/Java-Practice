package oops;

public class HelloWorldL {
	public static void main(String[] args) {
		String s="Hello World";	int count1=0, count2=0;
		System.out.println("String= "+ s);
		for(int i=0;i<s.length();i++) {
			if(Character.isLetter(s.charAt(0)));
			count1++;
			
		if(s.charAt(i)=='l') {
			count2++;
		}
		}
		System.out.println("Total Letters "+ count1++);
		System.out.println("L Letter "+ count2++);
}
	}

